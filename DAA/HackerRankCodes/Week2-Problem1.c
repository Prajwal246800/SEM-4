int camelcase(char* s)
{
    int wordCount = 1;  // At least one word is present (the first word in lowercase)
    // Iterate through the string to count uppercase letters
    for (int i = 0; s[i] != '\0'; i++)
    {
        if (isupper(s[i]))
        {
            wordCount++;
        }
    }
    return wordCount;
}
